int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 0)
		return (0);
	while (i * i < nb)
		i++;
	if (nb == 1)
		return (1);
	if (i >= nb)
		return (0);
	if ((nb % i) == 0)
		return (i);
	return (0);
}
